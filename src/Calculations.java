public class Calculations {
    /**
     *
     * @param op1
     * @param op2
     * @return sum of op1 and op2
     */
    public static double sum(double op1,double op2){
        return op1+op2;
    }
    public static double subtract(double op1,double op2){
        return op1-op2;
    }
    public static double division(double op1,double op2){
        return op1/op2;
    }
    public static double multiply(double op1,double op2){
        return op1*op2;
    }
}
